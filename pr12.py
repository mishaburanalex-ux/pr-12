# Виведемо заголовок практичної роботи
print("Практична робота 12. Обчислення НСД та НСК")
print("=" * 50)  # виводимо 50 символів '=' для візуального розділення

# Ввід чисел від користувача:
# Ми просимо ввести два цілих числа A і B.
# Використовуємо try/except, щоб ловити помилки вводу (якщо введуть не число).
try:
    a = int(input("Введіть перше число A: "))   # перше число
    b = int(input("Введіть друге число B: "))   # друге число
except ValueError:
    # Якщо введено не ціле число — повідомляємо і завершуємо програму.
    print("Будь ласка, введіть коректні цілі числа.")
    raise

# Збережемо початкові значення A і B в a1, b1,
# щоб потім обчислити НСК за формулою A*B/НСД.
a1, b1 = a, b

# Нижче реалізовано алгоритм Евкліда через віднімання:
# Поки a != b, від більшого числа віднімаємо менше.
# Коли числа стануть рівні — це і буде їх НСД.
while a != b:
    if a > b:
        # Якщо a більше, віднімаємо b від a
        a = a - b
    else:
        # Інакше (якщо b більше), віднімаємо a від b
        b = b - a

# Після циклу a == b, тому НСД = a (або b)
nsd = a
print(f"\nНСД({a1}, {b1}) = {nsd}")

# Обчислення НСК за формулою:
# НСК = (A * B) // НСД
# Використовуємо цілочисельне ділення '//' щоб отримати ціле число.
# (Зауваження: для нульових значень треба бути обережним — тут припускаємо, що НЕ обидва нулі)
if nsd != 0:
    nsk = (a1 * b1) // nsd
else:
    # Якщо НСД = 0 (тобто A = 0 і B = 0), то НСК визначати немає сенсу — позначимо як 0
    nsk = 0
print(f"НСК({a1}, {b1}) = {nsk}")

