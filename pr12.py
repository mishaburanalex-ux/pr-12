print("Практична робота 12. Обчислення НСД та НСК")
print("=" * 50)

# Введення чисел
try:
    a = int(input("Введіть перше число A: "))
    b = int(input("Введіть друге число B: "))
except ValueError:
    print("Будь ласка, введіть коректні цілі числа.")
    raise

# Збереження початкових значень для обчислення НСК
a1, b1 = a, b

# Алгоритм Евкліда: поки A ≠ B, від більшого віднімаємо менше
while a != b:
    if a > b:
        a = a - b
    else:
        b = b - a

nsd = a
print(f"\nНСД({a1}, {b1}) = {nsd}")

# Обчислення НСК за формулою: НСК = A * B / НСД
nsk = (a1 * b1) // nsd
print(f"НСК({a1}, {b1}) = {nsk}")

