# Задача 1: 60 цукерок і 45 яблук.
# Питання: Яка найбільша кількість однакових подарунків можна зібрати?
# Пояснення підходу: потрібно знайти НСД(60, 45).
print("\nЗадача 1: 60 цукерок і 45 яблук")
print("Яка найбільша кількість однакових подарунків?")

# Фіксуємо числа в змінні candies і apples — так простіше читати код.
candies = 60
apples = 45

# Застосуємо алгоритм Евкліда (через віднімання), як вище.
# Спочатку копіюємо числа в змінні a і b, бо будемо їх змінювати.
a, b = candies, apples

# Цикл: поки a не рівний b — віднімаємо менше від більшого.
while a != b:
    if a > b:
        a = a - b  # якщо a більше — зменшуємо a
    else:
        b = b - a  # інакше зменшуємо b

# Коли цикл закінчився — a==b і це НСД.
# Тому кількість подарунків = НСД (бо кожен подарунок буде містити однакову кількість предметів)
gifts = a
# Щоб знайти, скільки предметів в одному подарунку:
candies_per_gift = candies // gifts
apples_per_gift = apples // gifts

print(f"Відповідь: {gifts} подарунків (по {candies_per_gift} цукерок і {apples_per_gift} яблука)")

# Задача 2: два теплоходи, повертаються через 10 і 18 діб.
# Питання: через скільки діб вони зустрінуться одночасно у порту?
print("\nЗадача 2: Теплоходи повертаються через 10 і 18 діб")
print("Через скільки діб вони зустрінуться в порту знову?")

# Числа періодів руху
ship1 = 10
ship2 = 18

# Збережемо початкові значення (щоб не загубити їх при зміні)
a1_temp, b1_temp = ship1, ship2

# Використаємо алгоритм Евкліда (через віднімання) щоб знайти НСД(ship1, ship2)
a, b = ship1, ship2
while a != b:
    if a > b:
        a = a - b
    else:
        b = b - a

# Тепер a == b == НСД
nsd_ships = a

# Обчислюємо НСК за формулою: (ship1 * ship2) // nsd_ships
# Це дасть період, через який обидва одночасно повернуться
if nsd_ships != 0:
    nsk_ships = (a1_temp * b1_temp) // nsd_ships
else:
    nsk_ships = 0  # на випадок нульових періодів (тут неактуально)

print(f"Відповідь: через {nsk_ships} діб")

